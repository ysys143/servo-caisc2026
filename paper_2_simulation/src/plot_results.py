# Figure 2: R2 — FNR by topology x n_latent_classes (line plot)
# ---------------------------------------------------------------------------

def plot_r2_density(csv_path: str, out_path: str) -> None:
    rows = load_csv(csv_path)
    stats = aggregate(rows, ["topology", "n_latent_classes"], "false_novelty_rate")

    n_lcs = [20, 40, 80]
    topo_subset = ["isolated", "small_world", "shared"]
    line_styles = {"isolated": "o--", "small_world": "s-", "shared": "^:"}
    line_colors = {"isolated": "#aaaaaa", "small_world": "#555555", "shared": "#111111"}

    fig, ax = plt.subplots(figsize=(3.5, 2.4))

    for topo in topo_subset:
        means, stds = [], []
        for n_lc in n_lcs:
            key = (topo, str(n_lc))
            m, s = stats.get(key, (0.0, 0.0))
            means.append(m)
            stds.append(s)
        ax.errorbar(
            n_lcs, means, yerr=stds,
            fmt=line_styles[topo],
            color=line_colors[topo],
            markersize=4, linewidth=1.2, capsize=2,
            label=TOPOLOGY_LABELS[topo].replace("\n", " "),
        )

    ax.set_xlabel("Latent classes (hypothesis space complexity)")
    ax.set_ylabel("False Novelty Rate (FNR)")
    ax.set_xticks(n_lcs)
    ax.set_ylim(0, 1.05)
    ax.legend(loc="upper left", framealpha=0.9)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    fig.tight_layout(pad=0.5)
    fig.savefig(out_path, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_path}")



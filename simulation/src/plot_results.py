"""
Generate figures for the Illustrative Simulation section of paper_1.

Outputs (written to paper_2_simulation/results/):
  fig_fnr_primary.pdf  — grouped bar chart: FNR by topology x classifier_accuracy
  fig_fnr_r2_density.pdf — line chart: FNR by topology x n_latent_classes
  summary_primary.txt  — LaTeX-ready table rows for the paper
"""

import csv
import os
from collections import defaultdict

import numpy as np
import matplotlib
matplotlib.use("Agg")  # headless rendering, no display required
import matplotlib.pyplot as plt

# ---------------------------------------------------------------------------
# Paper-ready style: fits single column (3.5in wide), readable at 10pt
# ---------------------------------------------------------------------------
plt.rcParams.update({
    "font.family": "serif",
    "font.size": 9,
    "axes.titlesize": 9,
    "axes.labelsize": 9,
    "xtick.labelsize": 8,
    "ytick.labelsize": 8,
    "legend.fontsize": 8,
    "figure.dpi": 300,
    "pdf.fonttype": 42,   # embed fonts for journal submission
})

TOPOLOGY_ORDER = ["isolated", "lattice", "small_world", "shared"]
TOPOLOGY_LABELS = {
    "isolated": "Isolated\n(V-local)",
    "lattice": "Lattice\n(V-partial)",
    "small_world": "Small-world\n(V-improved)",
    "shared": "Shared\n(V-complete)",
}
ACC_COLORS = {0.60: "#bbbbbb", 0.75: "#666666", 0.90: "#111111"}
ACC_LABELS = {0.60: "acc=0.60", 0.75: "acc=0.75", 0.90: "acc=0.90"}


def load_csv(path: str) -> list[dict]:
    with open(path, newline="") as f:
        return list(csv.DictReader(f))


def aggregate(rows: list[dict], group_keys: list[str], metric: str):
    """Returns {group_key_tuple: (mean, std)} for the given metric."""
    buckets: dict[tuple, list[float]] = defaultdict(list)
    for row in rows:
        key = tuple(row[k] for k in group_keys)
        buckets[key].append(float(row[metric]))
    return {k: (np.mean(v), np.std(v)) for k, v in buckets.items()}


# ---------------------------------------------------------------------------
# Figure 1: Primary sweep — FNR by topology x classifier_accuracy
# ---------------------------------------------------------------------------

def plot_primary(csv_path: str, out_path: str, summary_path: str) -> None:
    rows = load_csv(csv_path)
    stats = aggregate(rows, ["topology", "classifier_accuracy"], "false_novelty_rate")
    recall_stats = aggregate(rows, ["topology", "classifier_accuracy"], "true_novelty_recall")

    accs = [0.60, 0.75, 0.90]
    n_topo = len(TOPOLOGY_ORDER)
    n_acc = len(accs)
    bar_width = 0.22
    group_gap = 0.08
    group_width = n_acc * bar_width + group_gap

    fig, ax = plt.subplots(figsize=(3.5, 2.6))

    x_positions = np.arange(n_topo) * group_width

    for i, acc in enumerate(accs):
        means = []
        stds = []
        for topo in TOPOLOGY_ORDER:
            key = (topo, str(acc))
            m, s = stats.get(key, (0.0, 0.0))
            means.append(m)
            stds.append(s)
        offsets = x_positions + i * bar_width - (n_acc - 1) * bar_width / 2
        ax.bar(
            offsets, means, bar_width,
            yerr=stds, capsize=2,
            color=ACC_COLORS[acc], label=ACC_LABELS[acc],
            error_kw={"elinewidth": 0.8, "ecolor": "#444444"},
        )

    ax.set_xticks(x_positions)
    ax.set_xticklabels([TOPOLOGY_LABELS[t] for t in TOPOLOGY_ORDER], ha="center")
    ax.set_ylabel("False Novelty Rate (FNR)")
    ax.set_ylim(0, 1.05)
    ax.axhline(1.0, color="#cccccc", linewidth=0.5, linestyle="--")
    ax.legend(loc="upper right", framealpha=0.9)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    fig.tight_layout(pad=0.5)
    fig.savefig(out_path, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_path}")

    # Write LaTeX-ready summary table
    with open(summary_path, "w") as f:
        f.write("% Auto-generated by plot_results.py\n")
        f.write("% Topology & V-completeness & FNR (acc=0.75) & True Recall (acc=0.75)\n")
        f.write("\\hline\n")
        labels = {
            "isolated": "Isolated",
            "lattice": "Lattice",
            "small_world": "Small-world",
            "shared": "Shared",
        }
        v_interp = {
            "isolated": "V-local (incomplete)",
            "lattice": "V-partial",
            "small_world": "V-improved",
            "shared": "V-complete",
        }
        for topo in TOPOLOGY_ORDER:
            key = (topo, "0.75")
            fnr_m, fnr_s = stats.get(key, (0, 0))
            rec_m, rec_s = recall_stats.get(key, (0, 0))
            f.write(
                f"{labels[topo]} & {v_interp[topo]} & "
                f"${fnr_m:.3f} \\pm {fnr_s:.3f}$ & "
                f"${rec_m:.3f} \\pm {rec_s:.3f}$ \\\\\n"
            )
    print(f"Saved: {summary_path}")


